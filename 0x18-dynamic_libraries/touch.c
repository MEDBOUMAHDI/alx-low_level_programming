#include <stdio.h>
/**
 * add - add two integers
 * @a: first unt
 * b: second int
 * Return: addition
*/
int add(int a, int b)
{
	return (a + b);

}
/**
 * sub - subtract  two integers
 * @a: first unt
 * b: second int
 * Return: subtracted final result
*/
int sub(int a, int b)
{
	return (a - b );

}
/**
 * mul - multiply two integers
 * @a: first unt
 * b: second int
 * Return: multiply
*/
int mul(int a, int b)
{
	return (a *  b);

}
/**
 * div - divided two integers
 * @a: first unt
 * b: second int
 * Return: division result
*/
int div(int a, int b)
{
	return (a / b);

}
/**
 * add - mod of  two integers
 * @a: first unt
 * b: second int
 * Return: final result
*/
int mod(int a, int b)
{
	return (a %  b);

}
